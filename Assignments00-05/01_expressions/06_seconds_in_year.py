def main():

    DAYS_IN_A_YEAR = 365

    HOURS_IN_A_DAY = 24
    
    MIN_IN_AN_HOUR = 60
    
    SEC_IN_A_DAY = 60

    SEC_IN_A_YEAR = DAYS_IN_A_YEAR * HOURS_IN_A_DAY * MIN_IN_AN_HOUR * SEC_IN_A_DAY

    print(f"There are {SEC_IN_A_YEAR} seconds in a year!")

if __name__ == "__main__":
    main()

